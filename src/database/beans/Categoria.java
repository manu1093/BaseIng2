package database.beans;

public class Categoria {
	private String nome;
	private String descrizione;
	
	public void setNome(String s)
	{
		nome=s;
	}
	public void setDescrizione(String s)
	{
		descrizione=s;
	}
	public String getNome()
	{
		return nome;
	}
	public String getDescrizione()
	{
		return descrizione;
	}
	

}
